package org.ctyc.mgt.utils;

public class NumberLabelUtils {

	public static String convertToGroupCode(int groupNumber) {
		
		if (groupNumber == 0){
			return "零";
		}
		if (groupNumber == 1){
			return "一";
		}
		if (groupNumber == 2){
			return "二";
		}
		if (groupNumber == 3){
			return "三";
		}
		if (groupNumber == 4){
			return "四";
		}
		if (groupNumber == 5){
			return "五";
		}
		if (groupNumber == 6){
			return "六";
		}
		if (groupNumber == 7){
			return "七";
		}
		if (groupNumber == 8){
			return "八";
		}
		if (groupNumber == 9){
			return "九";
		}
		if (groupNumber == 10){
			return "十";
		}
		if (groupNumber == 11){
			return "十一";
		}
		if (groupNumber == 12){
			return "十二";
		}
		if (groupNumber == 13){
			return "十三";
		}
		if (groupNumber == 14){
			return "十四";
		}
		if (groupNumber == 15){
			return "十五";
		}
		if (groupNumber == 16){
			return "十六";
		}
		if (groupNumber == 17){
			return "十七";
		}
		if (groupNumber == 18){
			return "十八";
		}
		if (groupNumber == 19){
			return "十九";
		}
		if (groupNumber == 20){
			return "二十";
		}
		if (groupNumber == 21){
			return "廿一";
		}
		if (groupNumber == 22){
			return "廿二";
		}
		if (groupNumber == 23){
			return "廿三";
		}
		if (groupNumber == 24){
			return "廿四";
		}
		if (groupNumber == 25){
			return "廿五";
		}
		if (groupNumber == 26){
			return "廿六";
		}
		if (groupNumber == 27){
			return "廿七";
		}
		if (groupNumber == 28){
			return "廿八";
		}
		if (groupNumber == 29){
			return "廿九";
		}
		
		
		return String.valueOf(groupNumber);
	}
}
